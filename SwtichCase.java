package com.onesoft.day12;

public class SwtichCase {
	public static void main(String[] args) {
		int a = 3;
		switch (a) {
		case 1: {
			System.out.println("moday");
		
		}
		case 2: {
			System.out.println("moday2");
		
		}
		case 3: {
			System.out.println("moday3");
		break;
		}
		default:
			System.out.println("Unexpected value: " + a);
		}
	}

}
